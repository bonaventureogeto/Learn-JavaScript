// Introduction to Data Types, Variables, and operators in JavaScript

// Data Types in JavaScript
console.log("Hello, World!"); // hello world is a string
console.log(237); // 237 is a number
console.log("237"); // 237 is string
console.log(true); // this is a boolean
console.log(false); // this is a boolean as well

/*
    Varibales in JavaScript:
    Variables are used to store data values.
    In JavaScript, we can declare variables using the var, let, and const keyword.
*/

let name; // variable declaration

name = "Jane Doe"; // variable initialization

console.log(name);

let name1 = "John Doe"; // variable declaration, initialization

console.log(name1);

// mutability - ability to change a variable/value

let number = 390.45; // variable declaration and initialization
number = 234; // overriding

console.log(number);

const number1 = 456; // const is immutable
// number1 = 200; 

console.log(number1);

